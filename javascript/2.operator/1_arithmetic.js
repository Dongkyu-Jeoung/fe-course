/**
 * 사칙연산
 * : +, -, *, /, %(모듈러, 나머지), **(지수)
 */

let a = 3 ;
let b = 5 ;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

// 짝수, 홀수 선택
let number1 = 100;
let number2 = 101;
console.log(number1%2);  // 나머지가 0이면 짝수, 1이면 홀수
console.log(number2%2);  

// 접합연산자(+) : '문자열' + '문자열'
console.log('한글' + '영어');  
console.log(10 + '10');  
console.log('합계=' + (10 + 10));  
